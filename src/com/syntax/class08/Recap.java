package com.syntax.class08;

public class Recap {

	public static void main(String[] args) {
		//while loop
		//we use when we don't know how many times the action should be repeated
		
		int a=1;
		
		while(a<=5) {
			System.out.println("code");
			a++;
		}

		//do while
		//we use when we dont know how many times the action should be repeated
		
		//do while vs while
		
		//while check condition first then does action if condition is true
		//do while does action and then check condition
		
		
		
		int c=1;
		
		do {
			System.out.println("Alex");
			c++;
		}while(c>5);
	}

}

package com.syntax.class014;

public class ToLowerCaseMethod {
    public static void main(String[] args){
        String name="Ali Kursun";
        System.out.println(name.toLowerCase());

        name="ali kursun";
        System.out.println(name.toLowerCase());

        name="ALI KURSUN";
        System.out.println(name.toLowerCase());
        System.out.println(name);
    }
}

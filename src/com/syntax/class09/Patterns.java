public class Patterns {

	public static void main(String[] args) {
		
		//************
		//************
		//************
		//************
		//************
		//************
		//6 rows, 12 columns
		
		for (int i=1; i<=6; i++) {
			for(int a=1; a<=12; a++) {
			System.out.print("*");
		}
		System.out.println();

	}
		System.out.println("--------square of numbers----------");
		
		for (int i=1; i<=4; i++) {
			for(int a=1; a<=5; a++) {
			System.out.print(a);
		}
		System.out.println();

}
		System.out.println("----------more example---------------");
		
		for(int b=1; b<=7; b++) {
			for(int c=1; c<=8; c++) {
				System.out.print(c);
			}
			System.out.println();
		}
		System.out.println("---------more example-------------------");
		
		for(int i=5; i>0; i--) {
		    for(int j=7; j>=1; j--) {
		        System.out.print(j);
		    }
		    System.out.println();
		}
		
}
}

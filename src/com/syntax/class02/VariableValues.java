package com.syntax.class02;

public class VariableValues {

	public static void main(String[] args) {
		byte variable1=13;
		System.out.println(variable1);
		variable1=14;
		System.out.println(variable1);
		
		
		short variable2=1258;
		System.out.println(variable2);
		variable2=1257;
		System.out.println(variable2);
		
		
		int variable3=1000;
		System.out.println(variable3);
		variable3=546413;
		System.out.println(variable3);
		
				
		long variable4=4523659785254310134L;
		System.out.println(variable4);
		variable4=6464246494642L;
		System.out.println(variable4);
		
		
		float variable5=6265646413.312311299f;
		System.out.println(variable5);
		variable5=6464294664194294422.66610601f;
		System.out.println(variable5);
		
		
		double variable6=45446464411.3541321213212126;
		System.out.println(variable6);
		variable6=41464294916.2468426246;
		System.out.println(variable6);
		
		
		char variable7='A';
		System.out.println(variable7);
		variable7='B';
		System.out.println(variable7);
		
		
		boolean variable8=true;
		System.out.println(variable8);
		variable8=false;
		System.out.println(variable8);
		

	}

}
